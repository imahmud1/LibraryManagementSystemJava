    private void jButtonreturnadd1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonreturnadd1ActionPerformed
        // TODO add your handling code here:
        String mid,books;
        
        mid = (String) jTextFieldMID1.getText();
        books = (String) jComboBoxBook1.getSelectedItem();
        
        SimpleDateFormat date_format1 = new SimpleDateFormat("yyyy-MM-dd");
        String DateFormat1 = date_format1.format(jReturnDate1.getDate());
        
        try{
            if(mid.isEmpty())
            {
                JOptionPane.showMessageDialog(this,"Please fill up the text field to add new book!");
            }
            else
            {
                insert = con1.prepareStatement("insert into issue_table(member_id,books,return_date) values(?,?,?)");
                insert.setString(1, mid);
                insert.setString(2, books);
                insert.setString(3, DateFormat1);
                
                int k = insert.executeUpdate();
                if(k == 1)
                {
                    JOptionPane.showMessageDialog(this,"Data inserted!");
                    
                    jTextFieldMID1.setText("");
                    jComboBoxBook1.setSelectedIndex(0);
                    jTextFieldMID1.requestFocus();
                   
                }
                else
                {
                    JOptionPane.showMessageDialog(this,"Error!");
                }
            }
        }catch(SQLException ex){
            System.out.println(ex);
        }
    }//GEN-LAST:event_jButtonreturnadd1ActionPerformed

