public void issue_table(){
    try{
            tbl = con1.prepareStatement("select * from issue_table");
            rs = tbl.executeQuery();
            
            javax.swing.table.DefaultTableModel df = (javax.swing.table.DefaultTableModel) jTable1.getModel();
            df.setRowCount(0);
            
            while(rs.next())
            {
                String id = String.valueOf(rs.getInt("id"));
                String name = rs.getString("member_id");
                String book = rs.getString("books");
                String idate = rs.getString("issue_date");
                String rdate = rs.getString("return_date");
                
                String row[] = {name,book,idate,rdate};
                df.addRow(row);
            }
        }catch(SQLException ex)
        {
            System.out.print(ex);
    }
}

